<p>This week we talked about the development of bio-mimetic and bio-inspired weaponry, armor, tactics, and camouflage. Because this week was pretty much a survey (We are going to pick a topic to present on next week), I&#8217;m going to do a rapid-fire summary of all the different technologies we touched on.</p>
</div>
</div>
</div>
<div class="sect2">
<h3 id="_weapons">Weapons</h3>
<div class="paragraph">
<p>Chemical weapons: poisons and irritants. They can be delivered as sprays (Skunks, Spitting Cobras), or on the ends of barbs (Caterpillars, Nettles), or through contact (Poison Dart Frogs, Caterpillars). They can vary from disgusting to deadly.</p>
</div>
<div class="paragraph">
<p>Projectiles: things that cause damage through projectile-launching are actually fairly rare in nature. Mostly, they use pneumatic cannons to drive seeds a long way (Mushrooms, Moss).</p>
</div>
<div class="paragraph">
<p>Spines: sticky, potentially infectious, uncomfortable. Hollow quills (Porcupines), quills filled with liquids (Nettles), prehensile tentacles with barbs (Sea Anemones), hooks (Burrs), or spiky leaves (Oak).</p>
</div>
<div class="paragraph">
<p>Mind Control: infectious parasites that modify behavior (Those ones that prey on Ants, Those ones that live in Sheep, Those ones that live in cats, and may be responsible for crazy cat ladies, but maybe not, the evidence is inconclusive, and they&#8217;re pretty rare anyway, although there are background levels pretty much everywhere a cat has been located in the past).</p>
</div>
</div>
<div class="sect2">
<h3 id="_armor">Armor</h3>
<div class="paragraph">
<p>Plates: Armadillos, dinosaurs</p>
</div>
<div class="paragraph">
<p>Tough hides: full of thick proteins, sometimes tougher than Kevlar.</p>
</div>
<div class="paragraph">
<p>Exoskeletons: (Shrimp, Lobster, Beetles)</p>
</div>
</div>
<div class="sect2">
<h3 id="_camo">Camo</h3>
<div class="paragraph">
<p>Passive Camo: look like your environment (Moths, Stick Bugs, certain Spiders)</p>
</div>
<div class="paragraph">
<p>Active Camo: change color, shape, and texture to hide. (Cuttlefish, Octopodes <sup class="footnote">[<a id="_footnoteref_1" class="footnote" href="#_footnotedef_1" title="View footnote.">1</a>]</sup>, and Chameleons]</p>
</div>
<div class="paragraph">
<p>Transparent Camo: They can&#8217;t see you if they can see through you! (Jellyfish, certain insects)</p>
</div>
<div class="paragraph">
<p>Distraction: Make them look somewhere else. (Octopodes (ink clouds), Angler-fish)</p>
</div>
</div>
<div class="sect2">
<h3 id="_tactics">Tactics</h3>
<div class="paragraph">
<p>Simple things: Don&#8217;t get hit. Be somewhere else. Be faster. Hide. Pretend to be poisonous. Look bigger. Regrow limbs.</p>
</div>
<div class="paragraph">
<p>Swarms: Signal to your friends (Bees), hide within a group (Zebras), move faster by flying together (Geese), overwhelm a superior opponent through numbers (Sea Lions). This is what I&#8217;m presenting on. I&#8217;m going to talk about how we can use swarms of smart-phones (because most people carry them on their person) to map areas, control crowd flow, perform surveillance, and create non-lethal weapons.</p>
</div>
<div class="paragraph">
<p>In lab this week, we experimented with using sound-based non-lethal weaponry. It was pretty cool, but not very effective. I blame human error and inexperience.</p>
</div>
