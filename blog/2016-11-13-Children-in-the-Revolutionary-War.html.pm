<div class="sect2">
<h3 id="_tell_us_about_sleds_on_boston_common">Tell us about "Sleds on Boston Common"</h3>
<div class="paragraph">
<p>"Sleds on Boston Common: A Story from the American Revolution" is a piece of historical fiction, written by Louise Borden. It is loosely based on some folk tales from the Boston area about the period of occupation of the British soldiers. It was praised for presenting both the sides of the war in a fair light, for focusing more on the children than the important events around them, and for being historically accurate. The story follows Prince Henry and his brothers, who all want to sled on Boston Common in their quest to get the soldiers to stop camping on the sledding runs.</p>
