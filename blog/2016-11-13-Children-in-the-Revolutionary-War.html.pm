<div class="sectionbody">
<div class="paragraph">
<p>My very generous Patreon supporters, Mark and Susan, wanted me to find out some things about the Revolutionary War for a presentation they intend to do. They are already big Revolutionary War buffs, and do a lot of reenactment, but this time they are going to be presenting to children - both 3rd grader and high-schoolers. As such, they had a lot of questions about how children participated in and were influenced by the war.</p>
</div>
</div>
</div>
<div class="sect2">
<h3 id="_did_girls_go_to_school">Did girls go to school?</h3>
<div class="paragraph">
<p>Often, children and wives would accompany soldiers on campaign. These children did not go to school, but were generally educated at home by the women. For girls who were not displaced by the war, they would be expected to go to school from 6-8 to learn writing and reading. Some of them would continue school, but because education wasn&#8217;t standardized, there was no consistent rule. In general, more children (and particularly girls) were schooled in the northern colonies than the southern colonies. New England was generally more literate because of Protestantism and its emphasis on reading the bible. Additionally, because school was expensive, usually only wealthy families could afford to send children after they learned to read.</p>
</div>
</div>
<div class="sect2">
<h3 id="_did_boys_go_to_school">Did boys go to school?</h3>
<div class="paragraph">
<p>Mostly! Again, from 6-8 boys were expected to learn reading and writing. Some were also required to learn Latin during that time. It was more common for boys to continue their education, especially if they were first-born or from wealthy families. Those boys who were displaced by the war usually forewent a formal education for assisting the soldiers or other aspects of the war effort - by running messages or playing marching drums or fifes. Boys as young as 16 could enlist, and boys as young as 7 acted as drummers.</p>
</div>
</div>
<div class="sect2">
<h3 id="_what_did_children_learn_in_school">What did children learn in school?</h3>
<div class="paragraph">
<p>Reading and writing of course, some elementary mathematics were taught, but curricula weren&#8217;t standardized until after 1837, in the wake of the popular school-reform movement. In general, students only learned what was believed necessary for their future careers. Specifically, students looking at higher education (and therefore disproportionately many wealthy students, as there were no federal subsidies), would learn rhetoric and languages, which were viewed as the most important skills for scholars. All children were taught religion - indeed, most schools were fundamentally religious - harking back to the start of public education in the colonies with the puritan settlers who believed everyone needed to be able to read the bible.</p>
</div>
<div class="paragraph">
<p>Many people, including Thomas Jefferson, believed that elementary education was of vital importance to democracy. Still, it was quite common for children to leave school to work on family farms or businesses much earlier than would be acceptable today - although there was no consistent criteria or age for graduation.</p>
</div>
</div>
<div class="sect2">
<h3 id="_which_children_were_put_into_apprenticeships">Which children were put into apprenticeships?</h3>
<div class="paragraph">
<p>In general, poorer children tended to be entered into apprenticeships. Apprenticeships were seen as a way to relieve the family of the burden of caring for the child, because the new apprentice&#8217;s master became responsible for their food, clothing, shelter, and education. This could obviously result in an exploitative relationship - and commonly did. The tradition of apprenticing children has its origins in indentured slavery. Slaves of all varieties - indentured and owned - were treated horribly throughout history. Some masters were kinder than others, of course, but there were few restrictions on how a master could treat their apprentice. By the time of the revolutionary war, apprentices were treated slightly better, on average, but it wouldn&#8217;t be until the 1900s that regulations about the treatment of apprentices met modern standards of kindness.</p>
</div>
<div class="paragraph">
<p>There was no standard age at which children entered into apprenticeships - although it was common for apprenticeships to end at the age of 21. Instead, the length of the apprenticeship would vary based on the age of the child in question. It was rare for an apprenticeship to last for less than 7 years, meaning that most children who did apprentice themselves did so before the age of 14. Some did so as young as 8 years of age. An apprentice&#8217;s education was wholly the responsibility of their master - usually, the apprenticeship agreement included some vague description of what would be taught, including the normal education in reading and writing, as well as the master&#8217;s craft. After the apprenticeship was complete, the child in question was taken to be a fully qualified, if somewhat junior, tradesman.</p>
</div>
</div>
<div class="sect2">
<h3 id="_what_age_were_children_expected_to_get_married">What age were children expected to get married?</h3>
<div class="paragraph">
<p>Data from the revolutionary period isn&#8217;t particularly clear. Quite apart from the lack of a census bureau or poll-takers, many marriages, especially among the lower and middle class, were formed purely on mutual consent, without any legal documentation at all. By the 1890s, a century later, the median age for marriage was 22-23. During the 1700s, children as young as 9 and 10 could marry, but it&#8217;s not clear how common that was. It is probable that girls were generally married younger than boys, but it&#8217;s not clear by how much. After the war, there was a general consensus that higher ages needed to be established before children could consent to various things - this actually caused problems, because no child under 14 could testify about any crimes perpetrated against them, such as abuse.</p>
</div>
</div>
<div class="sect2">
<h3 id="_when_did_new_parents_have_children">When did new parents have children?</h3>
<div class="paragraph">
<p>It was generally expected that they give birth directly after marriage - which means women would become pregnant anywhere from as soon as possible after puberty to their late twenties. Women would give birth an average of 7 times in their lives - wealthy families usually had more children, partly because they could support them, and partly because the women were more likely to survive childbirth.</p>
</div>
</div>
<div class="sect2">
<h3 id="_what_was_the_death_rate_of_children">What was the death rate of children?</h3>
<div class="paragraph">
<p>Data appears unclear <sup class="footnote">[<a id="_footnoteref_1" class="footnote" href="#_footnotedef_1" title="View footnote.">1</a>]</sup> (best known sources are from Sweden - many stillborn children and young deaths weren&#8217;t recorded), but nearly 3/10 children died before the age of 5 in 1775. 43.3% of children died before reaching age 18. (Although many were considered adults before that age. See above.) Because of these terrible mortality rates, Midwives in some areas were actually authorized to perform emergency baptisms to save the souls of children who wouldn&#8217;t survive long enough to see a priest. Women had a 1-2% chance of dying while giving birth (per birth) as well.</p>
</div>
<div class="paragraph">
<p>Slaves had worse infant and childhood mortality rates, with only 1 in four children reaching adulthood. There&#8217;s some evidence that a few babies were deliberately smothered to spare them a life of slavery.</p>
</div>
</div>
<div class="sect2">
<h3 id="_tell_us_a_bit_about_sybil_ludington">Tell us a bit about Sybil Ludington</h3>
<div class="paragraph">
<p>On April 26, 1777, Sybil Ludington rode 40 miles to warn her father&#8217;s militia about the approach of British soldiers. Well, maybe. The only historical account of her ride was written nearly 100 years later, by her great grandson. Despite the lack of solid evidence, Sybil&#8217;s ride made her famous as one of the heroines of the revolutionary war. She was only 16 when she rode her horse, Star, nearly twice as far as Paul Revere, unfortunately too late to allow the militia to reach Danbury, Connecticut before the British soldiers did. She has been commemorated by markers along her route, statues, on a postage stamp, and in a number of books:</p>
