<div class="paragraph">
<p>So last week I didn&#8217;t write about 2 of my classes - bionics and math. I didn&#8217;t write about math because I was still sorting out the paperwork to be in the correct course. Bionics, however, . . .</p>
</div>
<div class="paragraph">
<p>So our professor is currently emphasizing biomimicry. Plants and animals have had millions of years of evolution - they can do some amazing things. Technologies that imitate or interface with nature are called bionic technologies. Biomimicry, which is the first part of the course (roughly) can be approached in two ways.</p>
</div>
</div>
</div>
<div class="sect2">
<h3 id="_top_down">Top-down</h3>
<div class="paragraph">
<p>Say that you have a problem: it&#8217;s expensive to wash houses, and you wish they would wash themselves. In a top-down approach, you start with a problem, and then you find a solution. Sometimes, you will be able to find a solution by looking at nature, or by utilizing nature - you&#8217;ve just found a bionic solution. In the case of house-washing, you might notice that water rolls off of the leaves of waxy plants, particularly the lotus flower, taking dirt with it and leaving the plant clean.</p>
</div>
<div class="paragraph">
<p>You might then pull out your microscope, and figure out that that interaction is due to a series of microscopic bumps on the leaves. You would turn to your friend, who is a material scientist, and ask about how to cover houses in these bumps. Your friend would come up with a blend of stucco that has the same properties.</p>
</div>
<div class="paragraph">
<p>Now, when it rains, all of the dirt and grime on the house gets washed away without any additional effort.</p>
</div>
<div class="paragraph">
<p>This is a real development, and this sort of process gets followed all the time. Airplane turbulence? Look at how birds do it. Navigational beacons too hard to find? Imitate flowers that are shaped correctly to attract bats by concentrating sonar. Skyscrapers falling over? Look at the root systems of trees.</p>
</div>
</div>
<div class="sect2">
<h3 id="_bottom_up">Bottom-up</h3>
<div class="paragraph">
<p>Less common, but perhaps more exciting, is when observing nature leads you to invent something new - something that solves a problem you didn&#8217;t know you had.</p>
</div>
<div class="paragraph">
<p>Perhaps you&#8217;re out on a walk with your dog, and burrs get stuck on them. If you&#8217;re most people, you just pull them off. If you are George de Mestral, a swiss electrical engineer, you invent velcro based on the same principles.</p>
</div>
<div class="paragraph">
<p>Because adapting technologies from nature is such a creative process, the first parts of class have focused on removing creative blocks, and getting us to think outside the box. With that in mind, the first project is due Tuesday - come up with a bio-inspired piece of Oragami.</p>
</div>
</div>
<div class="sect2">
<h3 id="_i_hate_staples">I hate staples.</h3>
<div class="paragraph">
<p>I really hate staples.</p>
</div>
<div class="paragraph">
<p>They ruin any paper they&#8217;re attached to. They constantly jam in the stapler. They need special equipment to insert or remove.</p>
</div>
<div class="paragraph">
<p>Wouldn&#8217;t it be nice if there were an alternative? Let&#8217;s think of a few criteria for our staple-replacement:</p>
</div>
<div class="listingblock">
<div class="content">
<pre> - Doesn't harm the paper
 - No special equipment
 - Lays flat - needs to be stackable
 - Let's you read the pages inside without undoing anything
 - Doesn't come apart easily
 - Easy to do</pre>
</div>
</div>
<div class="paragraph">
<p>That&#8217;s all I came up with in about 5 minutes, but I&#8217;m sure you have your own staple-replacement-criteria.</p>
</div>
<div class="paragraph">
<p>So. What already holds together flat things?</p>
</div>
<div class="listingblock">
<div class="content">
<pre> - Binder clips
 - Paperclips
 - Pinecone stems
 - Leaf stems
 - Geckos
 - Sticky-notes
 - Tape
 - Flower stems
 - Large rocks
 - Friction</pre>
</div>
</div>
<div class="paragraph">
<p>Hmm.</p>
</div>
<div class="paragraph">
<p>Sticky-notes and tape require special equipment. Large rocks and friction aren&#8217;t good enough at keeping a bundle together. Binder clips and paperclips have A. already been invented and B. need special equipment. I could go the Gecko route or the stem route. Or maybe combine them. How do stems hold things on? (I just took apart a pinecone) It looks like each petal of the pinecone is stuck into a little slot, and held in by the petals above and below. That&#8217;s why it&#8217;s easier to remove the petals from a pinecone one way than the other.</p>
</div>
<div class="paragraph">
<p>(I spent about 30 minutes folding pieces of paper to try to make a gecko or a stem)</p>
</div>
<div class="paragraph">
<p>So how&#8217;s this:</p>
</div>
<div class="listingblock">
<div class="content">
<pre> 1. Take the top two corners of you stack of papers (I haven't tried this with more than 3 pages)
 2. Fold them over at 45 degrees to make two rabbit ears
 3. Fold the top of the paper down to the edge of the rabbit ears.</pre>
</div>
</div>
<div class="paragraph">
<p>It lays flat, it only requires paper, and it&#8217;s pretty sturdy - if you make the creases nice and sharp.</p>
</div>
<div class="paragraph">
<p>That&#8217;s what I&#8217;ll be turning in for my project on Tuesday.</p>
</div>
