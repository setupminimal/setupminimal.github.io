#lang pollen
<div id="preamble">
<div class="sectionbody">
<div class="paragraph">
<p>One of the things I enjoy, but very rarely have the time to actually indulge in, is studying the design of languages. All languages are beautiful, in one way or another, but I often find myself particularly engaged by constructed languages (also known as <a href="https://conlang.org">conlangs</a>, artificial languages, etc.).</p>
</div>
<div class="paragraph">
<p>One constructed language in particular that I think is really cool is <a href="https://lojban.org">Lojban</a>. Lojban (pronounced just like it&#8217;s spelled, but always with a soft j) is a constructed language designed from the ground up to be easy to learn, and easy to use accross cultural barriers. Naturally, this makes it quite different from most natural languages, and there&#8217;s still some debate as to whether they&#8217;ve sucseeded in this goal, but it&#8217;s still a pretty cool language.</p>
</div>
<div class="paragraph">
<p>There are a bunch of cool features of Lojban I could talk about, but in this case, I want to pick just one, one that is often overlooked:</p>
</div>
</div>
</div>
<div class="sect2">
<h3 id="_attitudinals">Attitudinals!</h3>
<div class="paragraph">
<p>The name isn&#8217;t very descriptive, honestly. Yes, they are words that describe attitudes, but there&#8217;s more to them than that. Let&#8217;s try that again.</p>
</div>
</div>
<div class="sect2">
<h3 id="_verbal_emoji">Verbal Emoji!</h3>
<div class="paragraph">
<p>This is a lot closer to the truth. Attitudinals are small words that look like this:</p>
</div>
<div class="listingblock">
<div class="content">
<pre>.ui</pre>
</div>
</div>
<div class="paragraph">
<p><em>.ui</em>, pronounced kind of like "Whee!" is an attitudinal that conveys happyness. By inserting <em>.ui</em> into your sentences, you can indicate that you are particularly happy about the thing that the word follows, kind of like an adjective. Or, you can use <em>.ui</em> all on it&#8217;s own, to indicate general happyness.</p>
</div>
<div class="paragraph">
<p><em>.ui</em> doesn&#8217;t have a direct English translation though. The closest might be "I&#8217;m happy!", but that doesn&#8217;t really capture how <em>.ui</em> can be used.</p>
</div>
<div class="paragraph">
<p>Really, I think it&#8217;s best to think of <em>.ui</em> as a smile. Just like a smile, it can be inserted into the conversation without interrupting the meaning of the other words, and it doesn&#8217;t seem as culturally awkward as walking up to someone and saying "I&#8217;m happy". Additionally, by making things like smiling explicit in Lojban, the language designers were able to clear up a lot of confusion that body language and tone could bring to a conversation when speaking with someone through text, or across a big cultural barrier, or even to an alien.</p>
</div>
<div class="paragraph">
<p>It&#8217;s also important not to confuse <em>.ui</em> with <em>.u&#8217;i</em>, which is pronounced like "Oooh, eee", and which indicates amusement. A lot of Lojban attitudinals contain an apostraphy, which in Lojban is pronunced a bit like a comma.</p>
</div>
<div class="paragraph">
<p>There are many attitudinals, of course, but I&#8217;m not going to just copy-and-paste a list from <a href="https://en.wikibooks.org/wiki/Lojban/Attitudinals">elsewhere on the internet</a>, because that&#8217;s just lazy. Instead, I&#8217;m going to hi-light what I think some of the most important are:</p>
</div>
<div class="paragraph">
<p><em>cai</em> (pronunced like "shy") is a word that strengthens attitudinals. It&#8217;s a bit like adding "very" to "I&#8217;m happy", or like smiling broadly instead of just smiling. You tak <em>cai</em> onto the end of whatever it&#8217;s modifying, so a big smile would be translated into Lojban as <em>.uicai</em> (pronounced: "Whee! shy").</p>
</div>
<div class="paragraph">
<p><em>cai</em> can be used with other attitudinals, of course, like <em>.uu</em> (pronunced kind of like "wu"), which is pity or sympathy. <em>.uucai</em> is like a sympathetic look, and a hand gently resting on someone&#8217;s shoulder.</p>
</div>
<div class="paragraph">
<p>There&#8217;s also another attitudinal modifier <sup class="footnote">[<a id="_footnoteref_1" class="footnote" href="#_footnotedef_1" title="View footnote.">1</a>]</sup> <em>nai</em>, which means the opposite. So <em>.uinai</em> would be like a frown, and <em>.uicainai</em> would be like a very big frown.</p>
</div>
<div class="paragraph">
<p>There&#8217;s nowhere I&#8217;m really going with this, except to show you a cool idea that I&#8217;ve actually never seen anywhere else. It&#8217;s a bit of a pity that attitudinals get eclipsed by the other features of Lojban when people come together to talk about the language.</p>
</div>
<div class="paragraph">
<p>I also think that it might be cool to try inserting attitudinals into your own conversations, at least with people in the know, or using them in place of emoji for ease of typing. I&#8217;ve barely scratched the surface of what attitudinals can convey (they also cover things like wants, categories of emotions, all the base emotions, combinations of emotions, agency, evidentiality, etc.), so go out, find some cool new words for emotions you never thought you&#8217;d be able to express in text, and use them to baffle your friends and befuddle your enemies. <em>.u&#8217;i</em></p>
</div>
