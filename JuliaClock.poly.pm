◊title{JuliaClock}

I created JuliaClock during High School for my required art credit. JuliaClock is a program that takes the current time, generates a unique ◊link[#:to "https://en.wikipedia.org/wiki/Julia_Set"]{Julia Set}, and then simulates painting it.

If you are interested in the ◊link[#:to "https://github.com/setupminimal/PyJulia"]{Source Code}, you may be able to get JuliaClock to run on your computer. It relies on the Python Image Library and on TK. If you'd rather just look at pretty pictures: (and videos, at the bottom)

